def match_ip():
    re_ip = r'(\d{1,2}|1\d\d|2[0-4]\d|25[0-5])(\.(\d{1,2}|1\d\d|2[0-4]\d|25[0-5])){3}'
    return re_ip


def match_mask():
    re_mask = r'(((128|192)|2(24|4[08]|5[245]))(\.(0|(128|192)|2((24)|(4[08])|(5[245])))){3})'
    return re_mask


def match_ip_and_mask():
    re_ip_and_mask = r'''((\d{1,2}|1\d\d|2[0-4]\d|25[0-5])(\.(\d{1,2}|1\d\d|2[0-4]\d|25[0-5])){3})
                         \/
                         (((128|192)|2(24|4[08]|5[245]))(\.(0|(128|192)|2((24)|(4[08])|(5[245])))){3})
                      '''
    return re_ip_and_mask


def match_ip_range():
    # 该地址段最后有一个空格符
    re_ip_range = r'''((\d{1,2}|1\d\d|2[0-4]\d|25[0-5])(\.(\d{1,2}|1\d\d|2[0-4]\d|25[0-5])){3})
                      -
                      ((\d{1,2}|1\d\d|2[0-4]\d|25[0-5])(\.(\d{1,2}|1\d\d|2[0-4]\d|25[0-5])){3}\s)
                   '''
    return re_ip_range
